    private void jBaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBaceptarActionPerformed
        try{
            db.insertProducto(jTnombre.getText(), Float.parseFloat(jTprecio.getText()), (int) jStock.getValue(), Integer.parseInt(jCiva.getSelectedItem().toString()));
            db.close();
            this.setVisible(false);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Introduce todos los datos correctamente", "Error en inseccion de datos", 0, null);
        }
    }//GEN-LAST:event_jBaceptarActionPerformed

