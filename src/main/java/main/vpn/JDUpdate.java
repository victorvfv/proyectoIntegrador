    private void jBactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBactualizarActionPerformed
        try{
            String nombre=jTnombre.getText().trim();
            int id=Integer.parseInt(jLID.getText().trim());
            int stock=(int) jStock.getValue();
            int iva=Integer.parseInt(jCiva.getItemAt(jCiva.getSelectedIndex()));
            float precio=Float.parseFloat(jTprecio.getText().trim());
            db.updateProducto(id, nombre, precio, stock, iva);
            db.close();
            this.setVisible(false);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Introduce todos los datos correctamente", "Error en inseccion de datos", 0, null);
        }
    }//GEN-LAST:event_jBactualizarActionPerformed

    private void jBeliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBeliminarActionPerformed
        db.deleteProducto(Integer.parseInt(jLID.getText().trim()));
        db.close();
        this.setVisible(false);
    }//GEN-LAST:event_jBeliminarActionPerformed

    private void jStockStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jStockStateChanged
        if(((int)jStock.getValue())<1){
            jStock.setValue(1);
        }
    }//GEN-LAST:event_jStockStateChanged

